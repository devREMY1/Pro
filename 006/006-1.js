// true + false;
// 8 / '2';
// 'number' + 5 + 1;
// 5 + 1 + 'number';
// 7 && 2;
// 2 && 7;
// null + 1;
// undefined + 1;
// 'five' + +'two';
// 'true' == true;
// false == 'false';
// null == '';
// |!!'false' == !!'true';
// '4' - 3;
// '4px' - 3;
// 0 || ('0' && 1);

console.log('true + false =', true + false); // 1
// Оскільки true дорівнює 1, а false дорівнює 0, то 1 + 0 = 1.

console.log('8 / "2" =', 8 / '2'); // 4
// Рядок "2" буде перетворено на число, тому результат буде 8 / 2 = 4.

console.log('"number" + 5 + 1 =', 'number' + 5 + 1); // "number51"
// Оскільки перший операнд — це рядок, всі наступні операнди будуть конкатеновані як рядки.

console.log('5 + 1 + "number" =', 5 + 1 + 'number'); // "6number"
// Спочатку виконується додавання чисел 5 + 1 = 6, а потім конкатенація з рядком "number".

console.log('7 && 2 =', 7 && 2); // 2
// Оператор && повертає останнє "істинне" значення, якщо всі операнди є істинними.

console.log('2 && 7 =', 2 && 7); // 7
// Аналогічно до попереднього, оператор && повертає останнє "істинне" значення.

console.log('null + 1 =', null + 1); // 1
// null перетворюється на 0, тому результат буде 0 + 1 = 1.

console.log('undefined + 1 =', undefined + 1); // NaN
// undefined не може бути перетворено на число, тому результат буде NaN.

console.log('"five" + + "two" =', 'five' + +'two'); // "fiveNaN"
// Оператор + перед рядком "two" намагається перетворити його на число, що не вдається, тому отримуємо NaN, яке конкатенується з "five".

console.log("'true' == true =", 'true' == true); // false
// Рядок 'true' не дорівнює булевому значенню true.

console.log("false == 'false' =", false == 'false'); // false
// Рядок 'false' не дорівнює булевому значенню false.

console.log("null == '' =", null == ''); // false
// null не дорівнює порожньому рядку.

console.log("!!'false' == !!'true' =", !!'false' == !!'true'); // true
// Обидва рядки "false" і "true" є істинними значеннями (truthy), тому після подвійного заперечення (!!) вони залишаються істинними.

console.log('"4" - 3 =', '4' - 3); // 1
// Рядок "4" перетворюється на число, тому відбувається арифметична операція 4 - 3 = 1.

console.log('"4px" - 3 =', '4px' - 3); // NaN
// Рядок "4px" не може бути перетворено на число, тому результатом буде NaN.

console.log('0 || "0" && 1 =', 0 || ('0' && 1)); // 1
// Оператор && має вищий пріоритет, тому спочатку обчислюється "0" && 1, що дорівнює 1. Потім 0 || 1 дорівнює 1.
